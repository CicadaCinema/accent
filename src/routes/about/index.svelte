<h1>Frequently Asked Questions</h1>

<h2>How are the displayed messages chosen?</h2>
<p>They are chosen at random out of the pool of all the user-submitted messages to date. However, you will never be
    shown any of your own messages.</p>

<h2>How many messages have already been submitted?</h2>
<p>A few hundred!</p>

<h2>What are the numbers that appear when I vote on a post?</h2>
<p>These are the like/dislike counts for each message.</p>
